			<h2>Workshop on Parallel Computing and Bio-Informatics</h2>
			<strong>Organised by sambalpur chapter</strong> <i>22nd and 23rd March, 2004</i>
			<hr>

			<p>Here 141 delegates had registered for the workshop. Out of them, there were 10 life members of OIT-sbp, 13 faculties from nearby colleges, rest being students from different branches like MCA, Engnieering. Msc comp. sc., Physics, Mathematics, Botany and Life Sciences.</p>

			<p>Professor Sudarsan Padhy, Utkal University, Vanivihar delivered talks on parallel computing in three different sessions.</p>

			<p>On 22.03.04, in the first session he illustrated the same through different examples like weather forecasting, image processing etc. emphasizing the importance of Parallel Alogorithms over serial Algorithms, and, taking different examples developed the problems on Parallel Algorithm. In the second Session some more developements in the problems of parallel alogorithms with multiprogramming and multi processing systems were considered.</p>

			<p>The uniqueness in the workshop was practical demonstrations as needed. Ajit kumar Nayak, Silicon Institute of Technology, Bhubaneswar, demonstrated in two different sessions about clustering of different work stations through Linux with the syntax to write programs using MPI. The programmes were demonstrated to the students. This approch creates a lot of interest among the students for the problems of parallel algorithms.</p>

			<p>There were Two Tea Breaks and One lunch break for all the delegates.</p>

			<p>On 23.03.04 Professor Padhy developed the problems of parallel algorithms and proved the results.</p>

			<p>Ajit Kumar Nayak demonstrated more programs of parallel algotithms to the delegates.</p>

			<p>In the after noon session Prof Padhy delivered a talk on Bio-Informatics which was followed by another talk from Prof A.P.Dash, G.M.College. There were two Tea Breaks and one lunch for all the delegates.</p>

			<p>A feed back Form were circulated to all deligates and their views were collected, which was enthusiastic and positive.</p>

