			<p>In october 2003, about eleven oits members from sambalpur area wanted that we may open the sambalpur chapter of oits. The president then located the earlier members around sambalpur area, and also asked some members outside orissa whether they would like to join this chapter. The executive committee which met on 16.11.2003 then approved that we may open this chapter as per article 6 of the bylaws, and convenor for this chapter, B Patel of G M College, sambalpur was intimated accordingly.</p>

